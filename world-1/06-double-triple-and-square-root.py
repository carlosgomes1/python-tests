# Make a program that receives an integer and shows on screen your double, triple and square root.

number = int(input('Enter an integer: '))

print(f'The number is {number}')
print(f'Double of the number is {number * 2}')
print(f'Triple of the number is {number * 3}')
print(f'Square root of the number is {number ** (1 / 2)}')
